const countLetters = function(sentence) {
  const countObj = {};
  for (let letter of sentence.replace(/ /g, "")) {
    if (countObj[letter]) {
      countObj[letter] += 1;
    } else {
      countObj[letter] = 1;
    }
  }
  return countObj;
};

